
#include <stdio.h>

int main() {
  int a = 3 + 5, b = 1, c; // a=8, b=1, c는 쓰레기값(초기화 안됨)
  int ap, bp;             // ap, bp도 쓰레기값

  ap = a++;   // 후위 증가: a의 현재값(8)을 ap에 대입 후, a를 1 증가 (a=9)
              // 결과: ap=8, a=9

  bp = ++b;   // 전위 증가: b를 먼저 1 증가 (b=2) 시킨 후, 그 값을 bp에 대입
              // 결과: bp=2, b=2

  b = 3 * (ap == 8); // 관계연산 먼저: ap(8) == 8 은 참(1)
                     // 계산: 3 * 1 = 3
                     // 결과: b=3

  c = 5 * (ap != 8); // 관계연산 먼저: ap(8) != 8 은 거짓(0)
                     // 계산: 5 * 0 = 0
                     // 결과: c=0

  // 최종 값 출력
  printf("%d %d %d %d %d", a, b, c, ap, bp);
  // a=9, b=3, c=0, ap=8, bp=2

  return 0;
}